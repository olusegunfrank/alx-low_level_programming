#include <stdio.h>
/**
 *  add - add two integers
 *  @a: first int
 *  @b: second int
 *  Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}

/**
 *  sub - sub two integers
 *  @a: first int
 *  @b: second int
 *  Return: subtract from final
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
 *  div - div of two integers
 *  @a: first int
 *  @b: second int
 *  Return: division
*/
int div(int a, int b)
{
	return (a / b);
}

/**
 *  mod - mod of two integers
 *  @a: first int
 *  @b: second int
 *  Return: result in mod
*/
int mod(int a, int b)
{
	return (a % b);
}

/**
 *  mul - multiply two integers
 *  @a: first int
 *  @b: second int
 *  Return: multiplication
*/
int mul(int a, int b)
{
	return (a * b);
}
